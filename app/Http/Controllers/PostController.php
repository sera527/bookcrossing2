     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $posts = Post::orderBy('created_at', 'desc')->simplePaginate(5);
        $count = Post::count();
        return view('recommendations', ['posts'=>$posts, 'count'=>$count, 'version' => IndexController::version(), 'countNot' => session()->get('count')]);
